    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $service = $this->serviceRepository->getById($id);
        $service->delete();

        return redirect()->route('service.index');

